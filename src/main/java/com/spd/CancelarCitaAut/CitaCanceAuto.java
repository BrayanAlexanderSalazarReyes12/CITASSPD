public class CitaCanceAuto {
    private String codCita;
    private String nitTransportadora;
    private String operacion;
    private String placa;
    private String cedulaConductor;
    private String manifiesto;
    private String fechaCita;

    public CitaCanceAuto() {
    }
    
    public String getCodCita() {
        return codCita;
    }

    public void setCodCita(String codCita) {
        this.codCita = codCita;
    }

    public String getNitTransportadora() {
        return nitTransportadora;
    }

    public void setNitTransportadora(String nitTransportadora) {
        this.nitTransportadora = nitTransportadora;
    }

    public String getOperacion() {
        return operacion;
    }

    public void setOperacion(String operacion) {
        this.operacion = operacion;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getCedulaConductor() {
        return cedulaConductor;
    }

    public void setCedulaConductor(String cedulaConductor) {
        this.cedulaConductor = cedulaConductor;
    }

    public String getManifiesto() {
        return manifiesto;
    }

    public void setManifiesto(String manifiesto) {
        this.manifiesto = manifiesto;
    }

    public String getFechaCita() {
        return fechaCita;
    }

    public void setFechaCita(String fechaCita) {
        this.fechaCita = fechaCita;
    }
    
    
}
